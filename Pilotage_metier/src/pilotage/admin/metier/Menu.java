package pilotage.admin.metier;

import java.io.Serializable;

public class Menu implements Serializable{

	private static final long serialVersionUID = -5436265243348757397L;
	private Integer id;
	private Integer id_parent;
	private Integer place;
	private String libelle;
	private String lien;
	private Integer icone;
	private Boolean interop;
	
	/**
	 * 
	 * @return
	 */
	public Boolean getInterop() {
		return interop;
	}
	
	/**
	 * 
	 * @param interop
	 */
	public void setInterop(Boolean interop) {
		this.interop = interop;
	}
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the id_parent
	 */
	public Integer getId_parent() {
		return id_parent;
	}
	/**
	 * @param id_parent the id_parent to set
	 */
	public void setId_parent(Integer id_parent) {
		this.id_parent = id_parent;
	}
	/**
	 * @return the place
	 */
	public Integer getPlace() {
		return place;
	}
	/**
	 * @param place the place to set
	 */
	public void setPlace(Integer place) {
		this.place = place;
	}
	/**
	 * @return the libelle
	 */
	public String getLibelle() {
		return libelle;
	}
	/**
	 * @param libelle the libelle to set
	 */
	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}
	/**
	 * @return the lien
	 */
	public String getLien() {
		return lien;
	}
	/**
	 * @param lien the lien to set
	 */
	public void setLien(String lien) {
		this.lien = lien;
	}
	/**
	 * @return the icone
	 */
	public Integer getIcone() {
		return icone;
	}
	/**
	 * @param icone the icone to set
	 */
	public void setIcone(Integer icone) {
		this.icone = icone;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id.hashCode();
		return result;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Menu other = (Menu) obj;
		if (id == other.id)
			return true;
		
		return false;
	}
	
	

}
