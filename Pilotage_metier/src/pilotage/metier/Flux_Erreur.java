package pilotage.metier;

import java.util.Date;

/**
 * @author xxu
 *
 */
public class Flux_Erreur {
	private Integer id;
	private Date date;
	private Flux_CFT flux;
	private String erreur;
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the date
	 */
	public Date getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(Date date) {
		this.date = date;
	}
	/**
	 * @return the flux
	 */
	public Flux_CFT getFlux() {
		return flux;
	}
	/**
	 * @param flux the flux to set
	 */
	public void setFlux(Flux_CFT flux) {
		this.flux = flux;
	}
	/**
	 * @return the erreur
	 */
	public String getErreur() {
		return erreur;
	}
	/**
	 * @param erreur the erreur to set
	 */
	public void setErreur(String erreur) {
		this.erreur = erreur;
	}
	
}
