package pilotage.metier;

import java.io.Serializable;
import java.util.Date;

public class Meteo_Envoi implements Serializable{

	private static final long serialVersionUID = 399661637699949248L;

	private Integer id;
	private Meteo_GroupeMeteo groupe;
	private Date date;
	private Date horaire;
	private Date date_envoi;
	private Date heure_envoi;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}

	public Meteo_GroupeMeteo getGroupe() {
		return groupe;
	}
	public void setGroupe(Meteo_GroupeMeteo groupe) {
		this.groupe = groupe;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Date getHoraire() {
		return horaire;
	}
	public void setHoraire(Date horaire) {
		this.horaire = horaire;
	}
	public Date getDate_envoi() {
		return date_envoi;
	}
	public void setDate_envoi(Date date_envoi) {
		this.date_envoi = date_envoi;
	}
	public Date getHeure_envoi() {
		return heure_envoi;
	}
	public void setHeure_envoi(Date heure_envoi) {
		this.heure_envoi = heure_envoi;
	}
	
}
