package pilotage.metier;

import java.io.Serializable;
import java.util.Date;

public class Incidents_Qualite implements Serializable {

	private static final long serialVersionUID = -3379066194254321908L;

	private Integer id;

	private Date dateEvenement;
	private String description;
	private String cause_raison;
	private String incidence;
	private String echeance;
	private Incidents_Qualite_Statut statut;
	private Date dateResolution;

	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Date getDateEvenement() {
		return dateEvenement;
	}
	public void setDateEvenement(Date dateEvenement) {
		this.dateEvenement = dateEvenement;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCause_raison() {
		return cause_raison;
	}
	public void setCause_raison(String cause_raison) {
		this.cause_raison = cause_raison;
	}
	public String getIncidence() {
		return incidence;
	}
	public void setIncidence(String incidence) {
		this.incidence = incidence;
	}
	public String getEcheance() {
		return echeance;
	}
	public void setEcheance(String echeance) {
		this.echeance = echeance;
	}
	public Incidents_Qualite_Statut getStatut() {
		return statut;
	}
	public void setStatut(Incidents_Qualite_Statut statut) {
		this.statut = statut;
	}
	public Date getDateResolution() {
		return dateResolution;
	}
	public void setDateResolution(Date dateResolution) {
		this.dateResolution = dateResolution;
	}
	
}