package pilotage.metier;

import java.io.Serializable;
import java.util.Date;

import org.joda.time.DateTime;

public class Meteo_GroupeMeteo_Horaire implements Serializable{

	private static final long serialVersionUID = -8584901186802412822L;
	
	private Integer id;
	private Meteo_GroupeMeteo groupeMeteo;
	private Date horaire;
	private Date dateException;
	private DateTime dateDebut;
	private DateTime dateFin;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Meteo_GroupeMeteo getGroupeMeteo() {
		return groupeMeteo;
	}
	public void setGroupeMeteo(Meteo_GroupeMeteo groupeMeteo) {
		this.groupeMeteo = groupeMeteo;
	}
	public Date getHoraire() {
		return horaire;
	}
	public void setHoraire(Date horaire) {
		this.horaire = horaire;
	}
	public Date getDateException() {
		return dateException;
	}
	public void setDateException(Date dateException) {
		this.dateException = dateException;
	}
	public DateTime getDateDebut() {
		return dateDebut;
	}
	public void setDateDebut(DateTime dateDebut) {
		this.dateDebut = dateDebut;
	}
	public DateTime getDateFin() {
		return dateFin;
	}
	public void setDateFin(DateTime dateFin) {
		this.dateFin = dateFin;
	}
}
