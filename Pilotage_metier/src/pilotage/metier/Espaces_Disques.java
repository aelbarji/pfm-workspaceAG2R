package pilotage.metier;

import java.util.Date;

public class Espaces_Disques {
	private Integer id;
	private Date date;
	private Disques disque;
	private Float espace;
	private Float seuil;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Disques getDisque() {
		return disque;
	}
	public void setDisque(Disques disque) {
		this.disque = disque;
	}
	public Float getEspace() {
		return espace;
	}
	public void setEspace(Float espace) {
		this.espace = espace;
	}
	public Float getSeuil() {
		return seuil;
	}
	public void setSeuil(Float seuil) {
		this.seuil = seuil;
	}
	
}
